public class Kullanici {
    private String adi;
    private int yas;

    public Kullanici(String adi, int yas) {
        this.adi = adi;
        this.yas = yas;
    }

    public String getAdi() {
        return adi;
    }

    public void setAdi(String adi) {
        this.adi = adi;
    }

    public int getYas() {
        return yas;
    }

    public void setYas(int yas) {
        this.yas = yas;
    }
}
